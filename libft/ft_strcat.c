char	*ft_strcat(char *s1, const char *s2)
{
	int		in1;
	int		in2;

	in1 = 0;
	in2 = 0;
	while (s1[in1] != '\0')
		in1++;
	while (s2[in2] != '\0')
	{
		s1[in1] = s2[in2];
		in1++;
		in2++;
	}
	s1[in1] = '\0';
	return (s1);
}
